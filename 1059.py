### Faça um programa que mostre os números pares entre 1 e 100, inclusive. Neste problema extremamente simples de repetição não há
### entrada. Imprima todos os números pares entre 1 e 100, inclusive se for o caso, um em cada linha. Q:1059

cem = 100
inicio = 2

while inicio <= cem:
	print(inicio)
	inicio = inicio + 2
